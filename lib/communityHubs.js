export const PLATFORM_FAMILIES = [
  "All",
  ...Array.from(new Set(PLATFORM_COMMUNITIES.map((platform) => platform.family))).sort((left, right) =>
    left.localeCompare(right),
  ),
];

export function getCommunityBySlug(slug) {
  return COMMUNITY_HUBS.find((community) => community.slug === slug) ?? null;
}

export function getCommunityById(id) {
  return COMMUNITY_HUBS.find((community) => community.id === Number(id)) ?? null;
}

export function searchPlatformCommunities(query = "") {
  const cleanQuery = String(query ?? "").trim();

  if (!cleanQuery) {
    return PLATFORM_COMMUNITIES;
  }

  return PLATFORM_COMMUNITIES.filter((platform) => matchesPlatformQuery(platform, cleanQuery)).sort(
    (left, right) => left.title.localeCompare(right.title),
  );
}

export function getFeaturedCommunities(limit = 6) {
  return COMMUNITY_HUBS.slice(0, Math.max(1, limit));
}
