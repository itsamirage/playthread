function normalizeSearchValue(value) {
  return String(value ?? "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function isSubsequenceMatch(query, candidate) {
  let queryIndex = 0;

  for (let index = 0; index < candidate.length && queryIndex < query.length; index += 1) {
    if (candidate[index] === query[queryIndex]) {
      queryIndex += 1;
    }
  }

  return queryIndex === query.length;
}

function matchesPlatformQuery(platform, query) {
  const normalizedQuery = normalizeSearchValue(query);

  if (!normalizedQuery) {
    return true;
  }

  const searchPool = normalizeSearchValue(
    [
      platform.title,
      platform.family,
      platform.subtitle,
      ...(platform.aliases ?? []),
    ].join(" "),
  );

  if (searchPool.includes(normalizedQuery)) {
    return true;
  }

  return searchPool
    .split(" ")
    .filter(Boolean)
    .some((token) => token.startsWith(normalizedQuery) || isSubsequenceMatch(normalizedQuery, token));
}

